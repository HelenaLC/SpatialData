require(SingleCellExperiment, quietly=TRUE)
x <- file.path("extdata", "blobs.zarr")
x <- system.file(x, package="SpatialData")

test_that("readElement()", {
    typ <- c(
        images="ImageArray", 
        labels="LabelArray", 
        points="PointFrame",
        shapes="ShapeFrame", 
        tables="SingleCellExperiment")
    for (l in names(typ)) {
        f <- paste0(toupper(substr(l, 1, 1)), substr(l, 2, nchar(l)-1))
        y <- list.files(file.path(x, l), full.names=TRUE)[1]
        if (l != "tables") {
            expect_is(get(paste0("read", f))(y), typ[l])
        } else {
            sce <- .readTables_basilisk(x)[[1]]
            expect_is(sce, typ[l])
            # check that 'int_metadata/colData' are handled properly
            # these should exist & be a named list
            sda <- int_metadata(sce)$spatialdata_attrs
            expect_is(sda, "list")
            # each element should be a scalar character
            nms <- c("region", "region_key", "instance_key")
            fun <- \(.) length(.) == 1 && is.character(.)
            expect_true(all(vapply(sda[nms], fun, logical(1))))
            expect_setequal(names(sda), nms)
            # '_key's should be present in 'int_colData'
            key <- unlist(sda[nms[-1]])
            icd <- names(int_colData(sce))
            expect_true(all(key %in% icd))
        }
    }
})

test_that("readSpatialData()", {
    expect_is(y <- readSpatialData(x), "SpatialData")
    a <- list(images=TRUE, labels=TRUE, shapes=TRUE, points=TRUE)#, tables=TRUE)
    for (. in names(a)) {
        # setting any layer to FALSE skips it
        b <- c(list(x=x), a); b[[.]] <- FALSE
        obj <- do.call(readSpatialData, b)
        expect_length(get(.)(obj), 0)
        # specifying non-existent element fails
        b <- c(list(x=x), a); b[[.]] <- 100
        expect_error(do.call(readSpatialData, b))
        b <- c(list(x=x), a); b[[.]] <- "."
        expect_error(do.call(readSpatialData, b))
        # specifying element name works
        f <- paste0(substr(., 1, 1), substr(., 2, nchar(.)-1), "Names")
        b <- c(list(x=x), a); b[[.]] <- get(f)(y)[1]
        expect_silent(do.call(readSpatialData, b))
    }
})
